package ex_01_Java_Basic;

public class Lab012_Println_vs_Print {
    public static void main(String[] args) {

        System.out.println("Hello World");
        System.out.println("Hello World2"); // Print with new line
// Output
//Hello World
//Hello World2
        System.out.print("Helloworld");
        System.out.print("Helloworld2"); // will not add new line
// Output
// HelloworldHelloworld2

    }
}
