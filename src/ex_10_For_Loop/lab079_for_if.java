package ex_10_For_Loop;

public class lab079_for_if {
    public static void main(String[] args) {
        for (int deep=0; deep < 18 ; deep++){
            if(deep > 15){
                System.out.println("Gift from Papa, Iphone");

            }
            else {
                System.out.println("No Gift as age is " + deep);
            }

        }
    }
}
