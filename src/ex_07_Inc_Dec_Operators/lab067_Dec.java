package ex_07_Inc_Dec_Operators;

public class lab067_Dec {
    public static void main(String[] args) {
        int a = 10;
        int result_post = a--;
        System.out.println(result_post);
        System.out.println(a);

        int b = 10;
        int result_post1 = --b;
        System.out.println(result_post1);
        System.out.println(b);


    }

}
